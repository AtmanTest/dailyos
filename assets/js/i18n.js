function t(key, vars) {
  const lang = localStorage.getItem('dailyos_lang') || 'fr';
  let str = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key])
    || (TRANSLATIONS['fr'] && TRANSLATIONS['fr'][key])
    || key;
  if (vars) {
    Object.keys(vars).forEach(k => {
      str = str.replace('{{' + k + '}}', vars[k]);
    });
  }
  return str;
}
window.t = t;
window.TRANSLATIONS = TRANSLATIONS;

function refreshLang() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  // Update document title
  document.title = t('app_name');
  document.querySelector('meta[name="description"]')?.setAttribute('content', t('desc'));
  // Update html lang
  const lang = localStorage.getItem('dailyos_lang') || 'fr';
  document.documentElement.lang = lang;
}
window.refreshLang = refreshLang;
